function yil=HangiYil(n)
if     (140000000<=n)&&(n<150000007)
       yil=2014;
elseif (130000000<=n)&&(n<140000000)
       yil=2013;
elseif (120000000<=n)&&(n<130000000)
       yil=2012;
elseif (110000000<=n)&&(n<120000000)
       yil=2011;
elseif (100000000<=n)&&(n<110000000)
       yil=2010;
elseif (150000000<=n)&&(n<160000000)
       yil=2015;
elseif (160000000<=n)&&(n<170000000)
       yil=2016;
elseif (170000000<=n)&&(n<180000000)
       yil=2017;
elseif (180000000<=n)&&(n<190000000)
       yil=2018;
elseif (190000000<=n)&&(n<200000000)
       yil=2019;
end